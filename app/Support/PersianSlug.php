<?php

namespace App\Support;

/**
 * ساخت slug فارسی‌پسند برای URL.
 *
 * برخلاف Str::slug() استاندارد لاراول (که چون فقط حروف لاتین رو می‌شناسه،
 * هر چی فارسی/عربی باشه رو کامل حذف می‌کنه و اگه عنوان کاملاً فارسی باشه
 * ممکنه slug خالی یا بی‌معنی دربیاد)، این نسخه حروف فارسی/عربی رو دست‌نخورده
 * نگه می‌داره - چون برای مخاطب و سئوی فارسی، slug فارسی هم خواناتره هم
 * بهتر ایندکس می‌شه.
 */
class PersianSlug
{
    public static function make(string $text, string $separator = '-'): string
    {
        // نرمال‌سازی کاراکترهای عربی رایج به معادل فارسی‌شون
        $text = str_replace(['ي', 'ك', 'ة'], ['ی', 'ک', 'ه'], $text);

        // هر چیزی که حرف یا عدد (فارسی/عربی/لاتین) نیست (فاصله، علامت
        // نگارشی، پرانتز و...) رو با یه فاصله‌ی ساده جایگزین کن
        $text = preg_replace('/[^\p{L}\p{N}]+/u', ' ', $text);

        $text = trim($text);
        $text = preg_replace('/\s+/u', $separator, $text);
        $text = mb_strtolower($text);

        // اگه بعد از پاک‌سازی چیزی نموند (مثلاً عنوان فقط علامت بود)،
        // یه رشته‌ی تصادفی برگردون تا slug خالی نشه
        return $text !== '' ? $text : \Illuminate\Support\Str::random(8);
    }
}
